<h1 class="title">LabelMe Help</h1>
                      
                  
<p>LabelMe is a WEB-based image annotation tool that allows researchers
to label images and share the annotations with the world.</p>

<p>LabelMe allows:</p>

<ul>
<li>Creation of user accounts: You will be able to create image databases for annotation.</li>
<li>Organization of images into collections: You can organize the images
into collections. You can also create collections inside collections
just as you would do with folders. When you download your database,
the collections will be organized into folders.</li>
<li>Uploading and image annotation: You can upload images into your
collections and annotate your images using the online LabelMe annotation
tool.</li>
</ul>

<h2>Getting started:</h2>

<p>
1. Start by creating a collection. Press the button <img class="labelBtn" style="cursor:text;" height="20px" src="../icons/new_collection.png" alt="New Collection"/> on the
top-right corner of the screen.<br>
2. To upload an image into the new collection, open the
collection that you have created by clicking on the collection icon
that will appear on the screen. Then you can upload an image by
clicking the icon <img  class="labelBtn" src="../icons/more_pictures.png" style="cursor:text;" alt="More Pictures" height="20px" />. We only allow .jpg images.<br>
3. Once an image is uploaded, you can annotate it by clicking on the
image thumbnail. This will open the annotation tool.
</p>

<p>
For navigation you can use the LabelMe hyperlinks or your web-browser
navigation buttons.
</p>

<h2>Privacy:</h2>

<p>
Currently, collections are unlisted. Unlisted collections are ones that anyone can view if they
have access to the URL, but will not be displayed in the set of public
folders.</p>

<p>As the goal of LabelMe
is to provide a tool for research, the images and annotations are
expected to become available to the research comunity without
restrictions.
</p>

<h2>Licenses</h2>

<p>
Please make sure that the images you upload are copyright-free and do not
contain inappropriate content.
The images that you upload and annotate will be considered part of the
public domain. As the goal of LabelMe
is to provide a tool for research, the images and annotations are
expected to become available to the research comunity without
restrictions.
</p>

<p>
You are free to post your collected database of images and annotations
on your own website.  For example, you may do this when you release
your database with your research publications.
</p>

<h2>Citation:</h2>

<p>If you use the dataset or any functions on this website, we would
appreciate it if you cite:<br />

